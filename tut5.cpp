#include<iostream>
using namespace std;
int main()
{
    int num1 , num2;
    cout<<"Enter the Value of num1: " ;// '<<' this is called insertion operator.
    cin>>num1; //'>>' this is called Extraction Operator.
   
    cout<<"Enter the Value of num2: " ;// '<<' this is called insertion operator.
    cin>>num2; //'>>' this is called Extraction Operator.
    
    cout<<"The sum is : "<<num1+num2;
    

    return 0;
}
